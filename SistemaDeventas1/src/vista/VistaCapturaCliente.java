    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Cliente cliente = new Cliente();
        TablaCliente controlCliente = new TablaCliente();

        if (!cajaNombre.getText().isEmpty() && !cajaApellidoPaterno.getText().isEmpty() && !cajaCurp.getText().isEmpty()) {
            //JOptionPane.showMessageDialog(null, "Correcto");

            if (!controlCliente.existeCliente(cajaCurp.getText().trim())) {

                cliente.setNombre(cajaNombre.getText().trim());
                cliente.setApellidoPaterno(cajaApellidoPaterno.getText().trim());
                cliente.setApellidoMaterno(cajaApellidoMaterno.getText().trim());
                cliente.setCurp(cajaCurp.getText().trim());
                cliente.setTelefono(cajaTelefono.getText().trim());
                cliente.setTipotarjeta((String) cajaTipotarjeta.getSelectedItem());
                cliente.setNumtarjeta(cajaNumTarjeta.getText().trim());
                cliente.setNumCasa(Integer.parseInt(cajaNumCasa.getText().trim()));
                cliente.setCalle(cajaCalle.getText().trim());
                cliente.setColonia(cajaColonia.getText().trim());
                

                if (controlCliente.guardar(cliente)) {
                    JOptionPane.showMessageDialog(null, "Registro Guardado");
                    cajaNombre.setBackground(Color.green);
                    cajaApellidoPaterno.setBackground(Color.green);
                    cajaApellidoMaterno.setBackground(Color.green);
                    cajaCurp.setBackground(Color.green);
                    cajaTelefono.setBackground(Color.green);
                    cajaTipotarjeta.setBackground(Color.green);
                    cajaNumTarjeta.setBackground(Color.green);
                    cajaNumCasa.setBackground(Color.green);
                    cajaCalle.setBackground(Color.green);
                    cajaColonia.setBackground(Color.green);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al Guardar");
                }

            } else {
                JOptionPane.showMessageDialog(null, "El cliente ya esta registrado en la Base de Datos.");
                    cajaNombre.setBackground(Color.white);
                    cajaApellidoPaterno.setBackground(Color.white);
                    cajaApellidoMaterno.setBackground(Color.white);
                    cajaCurp.setBackground(Color.white);
                    cajaTelefono.setBackground(Color.white);
                    cajaTipotarjeta.setBackground(Color.white);
                    cajaNumTarjeta.setBackground(Color.white);
                    cajaNumCasa.setBackground(Color.white);
                    cajaCalle.setBackground(Color.white);
                    cajaColonia.setBackground(Color.white);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Completa todos los campos");
                    cajaNombre.setBackground(Color.red);
                    cajaApellidoPaterno.setBackground(Color.red);
                    cajaApellidoMaterno.setBackground(Color.red);
                    cajaCurp.setBackground(Color.red);
                    cajaTelefono.setBackground(Color.red);
                    cajaTipotarjeta.setBackground(Color.red);
                    cajaNumTarjeta.setBackground(Color.red);
                    cajaNumCasa.setBackground(Color.red);
                    cajaCalle.setBackground(Color.red);
                    cajaColonia.setBackground(Color.red);
        }
        //metodo limpiar
        this.Limpiar();

    }//GEN-LAST:event_btnGuardarActionPerformed

