    private void CargarTablaVentas() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT cv.idCabeceraCompra, cv.Folio, cv.valorPagar AS total, cv.fechaCompra AS fecha, cv.horaCompra "
                + "FROM tb_cabecera_compra AS cv;";

        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            vistaGestionarCompras.jTable_compras = new JTable(model);
            vistaGestionarCompras.jScrollPane1.setViewportView(vistaGestionarCompras.jTable_compras);

            model.addColumn("NÂ°");//ID
            model.addColumn("Folio");
            model.addColumn("Total Pagar");
            model.addColumn("Fecha Compra");
            model.addColumn("Hora Compra");

            while (rs.next()) {
                Object fila[] = new Object[5];
                for (int i = 0; i < 5; i++) {
                    if (i == 5) {
                        String estado = String.valueOf(rs.getObject(i + 1));
                        if (estado.equalsIgnoreCase("1")) {
                            fila[i] = "Activo";
                        } else {
                            fila[i] = "Inactivo";
                        }
                    } else {
                        fila[i] = rs.getObject(i + 1);
                    }
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla de compras: " + e);
        }
        //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        jTable_compras.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_compras.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                 Folio  = (int) model.getValueAt(fila_point, columna_point);
                    
                }
            }
        });
    }
